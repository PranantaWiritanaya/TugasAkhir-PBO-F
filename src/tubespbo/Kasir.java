    private void btnSpnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSpnMouseClicked
        // TODO add your handling code here:
         if((Isiidpgw.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Id pegawai Tidak Boleh Kosong ");
        }else if((Isinama.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Nama Tidak Boleh Kosong");
        }else if((IsinoHp.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "No Hp kosong mohon di isi");
        }else if((Isialamat.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Alamat masih kosong mohon di isi");
        }
        else{
            
        }
        String Id_pegawai= Isiidpgw.getText();    
        String Nama = Isinama.getText();
        String  NoHp= IsinoHp.getText();
        String Alamat= Isialamat.getText();
   
        
            try {
                Connection con= Koneksidb.getkoneksi();
                String sql = "INSERT INTO pegawai VALUES (?,?,?,?)";
                PreparedStatement pe = con.prepareStatement(sql);
                    
                    pe.setString(1, Id_pegawai);
                    pe.setString(2, Nama);
                    pe.setString(3, NoHp);
                    pe.setString(4, Alamat);
                    pe.executeUpdate();
                JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
                tampil_kasir();
                reset();
            }catch(SQLException ex){
                System.out.println("erur");
            }
        
    }//GEN-LAST:event_btnSpnMouseClicked

    private void btnHpsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHpsMouseClicked
        // TODO add your handling code here:
           if(Tabelkasir.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(null,"Silahkan pilih baris data yang akan di ubah");
            return ;
        }
        if(JOptionPane.showConfirmDialog(null,"Apakah Yakin Untuk Menghapus ini?")==JOptionPane.OK_OPTION){
            try{

                String sql="DELETE FROM pegawai WHERE Id_pegawai='"+Isiidpgw.getText()+"'";
                PreparedStatement pst=conn.prepareStatement(sql);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data anda berhasil di hapus");
                
            }catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
            tampil_kasir();
            reset();
        }
    }//GEN-LAST:event_btnHpsMouseClicked

