 private void tampil_kasir(){
     // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id_pegawai");
        model.addColumn("Nama");
        model.addColumn("NoHp");
        model.addColumn("Alamat");
       
       
        //menampilkan data database kedalam tabel
        try {
            String sql = "SELECT * FROM `pegawai`";
            java.sql.Statement st=con.createStatement();
            java.sql.ResultSet re=st.executeQuery(sql);
            while(re.next()){
            model.addRow(new Object[]{re.getString(1),re.getString(2),re.getString(3),re.getString(4)});
            }
            Tabelkasir.setModel(model);
        } catch (Exception e) {
        }
    }
